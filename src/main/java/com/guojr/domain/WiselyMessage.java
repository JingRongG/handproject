package com.guojr.domain;

public class WiselyMessage {
    private String id;
    private String name;
    private String ip;
    private String message;

    public String getId() {
        return id;
    }

    public String getIp() {
        return ip;
    }

    public String getMessage() {
        return message;
    }

    public String getName() {
        return name;
    }
}
